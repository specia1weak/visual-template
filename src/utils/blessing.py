def bless(blesser: Blesser, drawing: bool = False):
    def draw(func):
        def wrapper(*args, **kwargs):
            print("函数{}获得了{}的祝福".format(func.__name__, blesser.name))
            blessing_drawing = blesser.value
            if drawing:
                print(blessing_drawing)
            result = func(*args, **kwargs)
            return result

        return wrapper

    return draw



